public class Status {
     
    boolean liga = true;
    boolean desliga = false;

    public boolean isLiga() {
        return liga;
    }

    public void setLiga(boolean liga) {
        this.liga = liga;
    }

    public boolean isDesliga() {
        return desliga;
    }

    public void setDesliga(boolean desliga) {
        this.desliga = desliga;
    }
}